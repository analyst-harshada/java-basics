public class loops {
    public static void main(String[] args) {
        // For Loop
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World");
        }

        // While Loop
        int j = 0;
        while (j < 5) {
            System.out.println("Hello World");
            j++;
        }

        // Do-While Loop
        int k = 0;
        do {
            System.out.println("Hello World");
            k++;
        } while (k < 5);
    }
    
}
